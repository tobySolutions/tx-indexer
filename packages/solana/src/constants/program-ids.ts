export function detectFacilitator(accountKeys: string[]): string | null {
  for (const facilitator of KNOWN_FACILITATORS) {
    if (accountKeys.includes(facilitator)) {
      if (facilitator === PAYAI_FACILITATOR) {
        return "payai";
      }
    }
  }
  return null;
}
